#include "main.h"
/**
 * print_sign - prints the sign of a number
 * @n: The character to print
 *
 * Return: Returns 1, 0 and -1
*/
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar(43);
		return (0);
		if (n == 0)
		{
			_putchar(0);
			return(0);
			if (n < 0)
			{
				_putchar(45);
				return (-1);
			}
		}
	}
	return (0);
}
